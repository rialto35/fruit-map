export function getMbtiProfile(mbti: string | undefined): MbtiProfile | null {
  if (!mbti || mbti === '모름' || mbti.length !== 4) return null;
  return MBTI_PROFILES[mbti] || null;
}

export function getTypeMbtiOneLiner(typeName: string, mbti: string | undefined): string {
  const typeKey = TYPE_KEY_MAP[typeName] || typeName;
  if (!mbti || mbti === '모름' || mbti.length !== 4) return '';
  return TYPE_MBTI_ONELINERS[`${typeKey}_${mbti}`] || '';
}

export function getConcernBrainTop3(concerns: string[]): { concern: string; top3: string[] }[] {
  return concerns
    .map(c => ({ concern: c, top3: CONCERN_BRAIN_TOP3[c] || CONCERN_BRAIN_TOP3['기타'] }))
    .filter(c => c.top3);
}

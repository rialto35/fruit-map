const TYPE_KEY_MAP: Record<string, string> = {
  '영적 수용형': '영적수용형', '관계 형성 우선형': '관계형성우선형',
  '논리적 탐구형': '논리적탐구형', '돌봄 회복형': '돌봄회복형',
  '핵심 압축형': '핵심압축형', '성장 가속형': '성장가속형', '표준 양육형': '표준양육형',
};

export function getOneLiner(typeName: string, concerns: string[]): string {
  const key = TYPE_KEY_MAP[typeName] || typeName;
  const data = ONELINERS[key];
  if (!data) return '';

  for (const c of concerns) {
    if (data.concerns[c]) return data.concerns[c];
  }
  return data.default;
}

/** 고민별 특화 문장만 반환. 매칭 없으면 빈 문자열. */
export function getConcernOneLiner(typeName: string, concerns: string[]): string {
  const key = TYPE_KEY_MAP[typeName] || typeName;
  const data = ONELINERS[key];
  if (!data) return '';
  for (const c of concerns) {
    if (data.concerns[c]) return data.concerns[c];
  }
  return '';
}
